import numpy as np

def size_grid(lifeform, grid_dim):
	grid = np.zeros(grid_dim)
	subgrid = lifeform
	cent = (grid.shape[0]//2 - subgrid.shape[0]//2 - 1, grid.shape[1]//2 - subgrid.shape[1]//2 - 1)
	grid[cent[0]:cent[0] + subgrid.shape[0], cent[1]:cent[1] + subgrid.shape[1]] = subgrid

	return grid

def random(grid_dim, density=0.2):
	grid = np.random.choice(
		[1, 0], grid_dim[0] * grid_dim[1], 
		p=[density, 1 - density]).reshape(grid_dim[0], grid_dim[1])
	return grid

